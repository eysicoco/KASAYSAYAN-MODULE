		 <h1 id="sbt1"><span class="bookmark-icon"></span>REPUBLIC ACT NO. 1425</h1>
		 <img src="../PICS/unit 1 pics/RA1.png" alt="Image Description">
		 <p>Ang Batas Republika 1425 na mas kilala sa tawag na Batas Rizal ay pinangunahan ng dating pinuno ng Pambansang Kapulungan ng Edukasyon na si Sen. Jose P. Laurel. Bago ito mapagtibay noong Hunyo 12, 1956, dumaan ang batas na ito sa mga umaatikabong debate sa loob ng Senado at Kongreso.<br><br>
		 <p>Tinawag itong House Bill 5561 sa kongreso na pinangunahan ni Cong. Jacobo Gonzales at tinawag naman itong Senate Bill 438 sa Senado na pinangunahan naman ni Sen. Claro M. Recto. Hindi makakapagtaka na sila ang mga pinunong nagtaguyod sa batas na ito, dahil kung babalikan ang kasaysayan, malinaw na may marubdob na pagmamahal sa bayan ang dalawang ito. Si Gonzales ay nakipaglaban upang mapalaya ang kanyang mga kababayang sakdalista at si Recto naman ay malinaw na ipinaglaban ang soberanya ng Pilipinas labas sa Estados Unidos.</p>

</p><br><br><h1 id="sbt2"><span class="bookmark-icon"></span>RIZAL: INSPIRASYON NG BAGONG HENERASYON</h1><p>
Ang pangunahing layunin ng mga mga nagtaguyod sa batas na ito ay muling pag-alabin ang diwa ng nasyonalismo sa mga susunod na henerasyon ng mga Pilipino sa pamamagitan ng pagsama sa kurikulum ng lahat ng paaralan, pampubliko man o pribado ang kurso sa pag-aaral ng buhay, mga ginawa at isinulat ni Jose Rizal, partikular na ang kanyang dalawang nobela na Noli Me Tangere at El Filibusterismo. Naniniwala sila na si Rizal ay maaaring magsilbing inspirasyon sa atin, lalo na sa mga kabataan. Bukod dito, layunin din ng batas na ito na parangalan si Rizal at ang iba pa nating mga bayani sa lahat ng kanilang mga ginawa para sa bayan.

</p><br><br><h1 id="sbt3"><span class="bookmark-icon"></span>BATAS RIZAL: KAPARIS NG RH BILL</h1><p>
Katulad ng RH Bill, hindi naging madali ang pagpasa ng batas na ito dahil sa malakas na pagtutol ng Simbahang Katoliko. Naniniwala ang simbahan at ang mga mambabatas na pumanig sa kanila na ang dalawang nobela ni Rizal ay naglalaman ng mga pahayag na laban sa simbahan. Ayon sa kanila, ang sinumang makabasa ng Noli Me Tangere at El Filibusterismo ay maaaring mawalan ng pananampalataya o sumalungat sa mga itinuturo ng simbahan.Iba’t ibang taktika ang ginamit nila upang takutin ang mga nagtataguyod sa batas na ito. Nariyang magsasara daw ang lahat ng paaaralang pag-aari ng simbahan sa oras na maipatupad ito at hindi daw makakaasa ng suporta mula sa mga katoliko ang mga nagtaguyod nito sa mga susunod na halalan. Ngunit kaparis ng RH Bill, naipatupad ang Batas Rizal sa kabila ng malakas na pagtutol ng simbahan

</p><br><br><h1 id="sbt4"><span class="bookmark-icon"></span>BATAS RIZAL: SA NAKARAANG 50 TAON</h1>
<p>Limang dekada na ang nakalipas mula ng maipatupad ang Batas Rizal. Ngunit nakatulong nga ba ang pag-aaral ng buhay, mga ginawa at isinulat ni Jose Rizal upang makamit ang mga adhikain ng mga nagtaguyod sa batas na ito? Sa mababaw na pagtingin, masasabi nating naging matagumpay ito, dahil sa halos lahat ng plaza sa ating bansa ay may bantayog si Rizal. Kilala siya ng lahat ng mga Pilipino bilang ating pambansang bayani. Madalas din nating gamitin ang mga salitang iniwan niya, katulad ng walang-kamatayang “ang kabataan ang pag-asa ng bayan.”
</p>


		 
		 
